// 임시 검증 스크립트: node test-parse.mjs
import { parseTransactions } from "./src/parse.js";

let pass = 0, fail = 0;
function check(name, text, expected) {
  const { expenses } = parseTransactions(text);
  const got = expenses.map((e) => `${e.merchant}|${e.amount}`);
  const ok = JSON.stringify(got) === JSON.stringify(expected);
  if (ok) { pass++; console.log(`✅ ${name}`); }
  else {
    fail++;
    console.log(`❌ ${name}\n   기대: ${JSON.stringify(expected)}\n   실제: ${JSON.stringify(got)}`);
  }
}

// 1) 기본: 카카오페이 스타일 (부호 + 잔액 줄)
check("기본 거래+잔액", `
6월 7일 일요일
스타벅스 강남점 -5,500원
16:38 1,234,500원
GS25 역삼점 -2,300원
17:02 1,232,200원
`, ["스타벅스 강남점|5500", "GS25 역삼점|2300"]);

// 2) 수입 제외
check("수입 제외", `
6월 7일 일요일
이자입금 +1,200원
09:00 1,500,000원
맥도날드 -7,900원
12:10 1,492,100원
`, ["맥도날드|7900"]);

// 3) OCR이 부호를 놓친 지출 (가맹점 글자 있음)
check("부호 누락 지출", `
6월 7일 일요일
파리바게뜨 4,800원
10:11 1,400,000원
`, ["파리바게뜨|4800"]);

// 4) [신규] 쉼표를 마침표로 오인식
check("마침표 천단위", `
6월 7일 일요일
올리브영 -12.500원
13:00 1,388,000원
`, ["올리브영|12500"]);

// 5) [신규] '원' 글자를 통째로 놓침 (부호는 있음)
check("원 누락 + 부호", `
6월 7일 일요일
배달의민족 -23,000
19:30 1,365,000원
`, ["배달의민족|23000"]);

// 6) [신규] 날짜·범위 숫자를 금액으로 오인하지 않음
check("날짜 오인 방지", `
2025-06-07 거래내역
6월 7일 일요일
컴포즈커피 -1,800원
08:05 1,363,200원
`, ["컴포즈커피|1800"]);

// 7) 잔액을 지출로 오인하지 않음 (부호 없는 큰 숫자)
check("잔액 구분", `
6월 7일 일요일
교보문고 -15,000원
1,348,200원
다이소 -3,000원
1,345,200원
`, ["교보문고|15000", "다이소|3000"]);

// 8) 요약 줄 무시
check("요약 무시", `
이번 달 소비 532,000원
지난달보다 12% 덜 쓰고 있어요
6월 7일 일요일
한솥도시락 -6,500원
12:00 1,338,700원
`, ["한솥도시락|6500"]);

// 9) 시간 줄에 잔액이 같이 (토스 스타일)
check("시간+잔액 한 줄", `
6월 7일 일요일
카카오T 택시 -8,400원
22:15 1,330,300원
`, ["카카오T 택시|8400"]);

// 10) '원' 누락 구제가 잔액(부호 없음)을 건드리지 않음
check("원 누락 구제 안전성", `
6월 7일 일요일
쿠팡이츠 -18,500
21:00 1,311,800
`, ["쿠팡이츠|18500"]);

console.log(`\n결과: ${pass} 통과 / ${fail} 실패`);
