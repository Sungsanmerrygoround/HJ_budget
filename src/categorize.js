/**
 * 가맹점명 → 카테고리 (못 맞추면 '기타')
 */
export function categorize(merchant) {
  if (!merchant) return "기타";
  const name = merchant.toLowerCase();
  for (const [category, keywords] of RULES) {
    if (keywords.some((kw) => name.includes(kw))) return category;
  }
  return "기타";
}
